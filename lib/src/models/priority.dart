/// 事件优先级枚举
///
/// 定义了事件的处理优先级：
/// - [low]: 低优先级，用于不重要的后台任务
/// - [normal]: 普通优先级（默认），用于一般的事件处理
/// - [high]: 高优先级，用于重要的业务逻辑
/// - [critical]: 关键优先级，用于系统关键操作
enum Priority {
  /// 低优先级
  ///
  /// 用于不重要的后台任务，如：
  /// - 数据同步
  /// - 缓存清理
  /// - 日志记录
  low,

  /// 普通优先级（默认）
  ///
  /// 用于一般的事件处理，如：
  /// - UI更新
  /// - 用户操作响应
  /// - 一般的数据处理
  normal,

  /// 高优先级
  ///
  /// 用于重要的业务逻辑，如：
  /// - 关键数据更新
  /// - 重要的状态变更
  /// - 用户关键操作
  high,

  /// 关键优先级
  ///
  /// 用于系统关键操作，如：
  /// - 系统错误处理
  /// - 安全相关操作
  /// - 核心功能状态变更
  critical,
}
